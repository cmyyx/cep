/**
 * Auto-generate src/data/characters/index.ts from JSON files in the directory.
 *
 * Usage: node scripts/generate-characters-index.mjs
 *
 * Scans src/data/characters/*.json (excluding all.json) and generates
 * an index.ts with imports and a lookup map for each character.
 */

import { readdirSync, readFileSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const projectRoot = join(__dirname, '..')
const DATA_DIR = join(projectRoot, 'src', 'data', 'characters')
const OUT_FILE = join(DATA_DIR, 'index.ts')

const files = readdirSync(DATA_DIR)
  .filter((f) => f.endsWith('.json') && f !== 'all.json')
  .sort()

if (files.length === 0) {
  console.error('No character JSON files found in', DATA_DIR)
  process.exit(1)
}

const entries = []

for (const file of files) {
  const content = readFileSync(join(DATA_DIR, file), 'utf-8')
  const data = JSON.parse(content)
  const id = data.id || file.replace('.json', '')
  // Derive import name: camelCase the filename
  const baseName = file.replace('.json', '')
  const importName = baseName.replace(/[-_]([a-z])/g, (_, c) => c.toUpperCase())
  entries.push({
    file,
    importName,
    id,
    name: data.name || id,
  })
  console.log(`  ${file} → ${importName} (${data.name || id})`)
}

// Generate import lines
const imports = entries
  .map((e) => `import ${e.importName}Data from './${e.file}'`)
  .join('\n')

// Generate record entries
const recordEntries = entries
  .map((e) => `  '${e.id}': ${e.importName}Data as CharacterGuideData,`)
  .join('\n')

// Generate list entries
const listEntries = entries
  .map((e) => `  ${e.importName}Data as CharacterGuideData,`)
  .join('\n')

const output = `// Auto-generated by scripts/generate-characters-index.mjs
// Do not edit manually.

import type { CharacterGuideData } from '@/types/character-guide'

${imports}

/** All character guide data, keyed by lowercase ID */
export const characterGuideData: Record<string, CharacterGuideData> = {
${recordEntries}
}

/** Ordered list of character guide data */
export const characterGuideList: CharacterGuideData[] = [
${listEntries}
]

export type { CharacterGuideData }
`

try {
  writeFileSync(OUT_FILE, output)
  console.log(`\nGenerated ${OUT_FILE} with ${entries.length} characters.`)
} catch (err) {
  console.error(`Failed to write ${OUT_FILE}:`, err.message || err)
  process.exit(1)
}
